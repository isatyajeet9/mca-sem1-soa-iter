greeting = "hello students. How are you doing "

print(greeting.count("you"))
print(greeting.find("s"))
print(greeting.rfind("s"))
print(greeting.capitalize())
print(greeting.lower())
print(greeting.upper())
print(greeting.swapcase())
print(greeting.istitle())
print(greeting.replace("hello","hai"))
print(greeting.strip())
print(greeting.split())
print(greeting.partition("."))
print(greeting.startswith("how"))
print(greeting.endswith("."))

