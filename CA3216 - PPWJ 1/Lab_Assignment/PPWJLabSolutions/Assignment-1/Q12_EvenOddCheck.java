// Question: Check whether a number is even or odd using modulus operator

public class Q12_EvenOddCheck {
    public static void main(String[] args) {
        int n = 11;
        if (n % 2 == 0)
            System.out.println("Even");
        else
            System.out.println("Odd");
    }
}
